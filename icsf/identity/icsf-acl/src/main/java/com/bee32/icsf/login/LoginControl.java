package com.bee32.icsf.login;

import javax.free.Control;

public class LoginControl
        extends Control {

    private static final long serialVersionUID = 1L;

    public LoginControl() {
        super();
    }

    public LoginControl(String message, Throwable cause) {
        super(message, cause);
    }

    public LoginControl(String message) {
        super(message);
    }

    public LoginControl(Throwable cause) {
        super(cause);
    }

}
