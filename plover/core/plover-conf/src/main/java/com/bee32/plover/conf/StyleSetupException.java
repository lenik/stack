package com.bee32.plover.conf;

public class StyleSetupException
        extends Exception {

    private static final long serialVersionUID = 1L;

    public StyleSetupException() {
        super();
    }

    public StyleSetupException(String message, Throwable cause) {
        super(message, cause);
    }

    public StyleSetupException(String message) {
        super(message);
    }

    public StyleSetupException(Throwable cause) {
        super(cause);
    }

}
