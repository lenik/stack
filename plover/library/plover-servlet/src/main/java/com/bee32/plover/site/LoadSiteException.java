package com.bee32.plover.site;

public class LoadSiteException
        extends Exception {

    private static final long serialVersionUID = 1L;

    public LoadSiteException() {
        super();
    }

    public LoadSiteException(String message, Throwable cause) {
        super(message, cause);
    }

    public LoadSiteException(String message) {
        super(message);
    }

    public LoadSiteException(Throwable cause) {
        super(cause);
    }

}
